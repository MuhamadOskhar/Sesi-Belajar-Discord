// Cara membuat variable di javascript
var a = 'a';
let b = 'b';
const c = 'c';

// Typedata yang dipelajar saat ini = (string, integer, float, boolean)
let nama = 'oskhar';     // String
let umur = 22;           // Integer
let belumMenikah = true; // Boolean
let beratBadan = 50.4;   // Float

// Operator aritmatika
let angkaA = 10;
let angkaB = 20;
console.log(angkaA + angkaB);
console.log(angkaA - angkaB);
console.log(angkaA * angkaB);
console.log(angkaA / angkaB);
console.log(angkaA % angkaB);

// Oprasi aritmatika bisa dibungkus atau dimasukan pada sebuah variable
let hasilPerjumlahan = 10 + 10;
console.log(hasilPerjumlahan);

// Setelah melakukan deklarasi pada variabel tertentu, programmer tidak perlu melakukan deklarasi lagi (tidak pakai let dua kali)
hasilPerjumlahan = 7 + 8
console.log(hasilPerjumlahan);